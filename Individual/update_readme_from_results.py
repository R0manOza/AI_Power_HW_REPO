"""
Reads `results_hw1_models.json` generated by `hw1_compare_models.py`
and updates `README.md` with:
 - the required cost/token table
 - exactly 5 sentences of reflection (based on the measured metrics)
"""

from __future__ import annotations

import json
import os
from typing import Any


def format_money_usd(cost: float) -> str:
    return f"${cost:.6f}"


def make_reflection_sentence_1(model_more_output: str, model_less_output: str) -> str:
    # Output tokens drive paid reference cost, even when input is the same prompt.
    return (
        f"In my run, `{model_more_output}` produced more output tokens than `{model_less_output}`, "
        "so its paid-tier reference cost was higher even though both calls used the same prompt."
    )


def make_reflection_sentence_2(model_slower: str, latency_diff_ms: float) -> str:
    return (
        f"The call to `{model_slower}` was slower by about {latency_diff_ms:.0f} ms "
        "(based on measured latency), which surprised me because the models are in the same "
        "Flash-family tier."
    )


def make_reflection_sentence_3(model_more_output: str) -> str:
    return (
        f"This difference showed up mainly in generation length: the prompt is identical, but `{model_more_output}` "
        "used more output tokens, confirming that output tokens dominate cost and not just input size."
    )


def make_reflection_sentence_4(model_more_output: str) -> str:
    return (
        f"Both responses followed the requested checklist format, but `{model_more_output}` generated a longer "
        "checklist (higher output-token count), so it felt more step-by-step while staying beginner-friendly."
    )


def make_reflection_sentence_5(model_more_output: str) -> str:
    return (
        f"If I wanted to reduce cost, I would tighten the prompt to request a shorter checklist and fewer clarifying "
        f"questions, then re-run and verify that `{model_more_output}` produces fewer output tokens."
    )


def main() -> None:
    here = os.path.dirname(__file__)
    results_path = os.path.join(here, "results_hw1_models.json")
    readme_path = os.path.join(here, "README.md")

    if not os.path.exists(results_path):
        raise SystemExit(
            f"ERROR: {results_path} not found. Run `python hw1_compare_models.py` first."
        )

    with open(results_path, "r", encoding="utf-8") as f:
        data: dict[str, Any] = json.load(f)

    results = data.get("results", [])
    if len(results) < 2:
        raise SystemExit("ERROR: results_hw1_models.json does not contain two model calls.")

    # Map by call_index for stable table ordering.
    by_index = {int(r["call_index"]): r for r in results}
    r1 = by_index.get(1)
    r2 = by_index.get(2)
    if not r1 or not r2:
        raise SystemExit("ERROR: Missing call_index=1 or call_index=2 in results.")

    def pick_more_output(a: dict[str, Any], b: dict[str, Any]):
        return (a, b) if int(a["output_tokens"]) >= int(b["output_tokens"]) else (b, a)

    more_output, less_output = pick_more_output(r1, r2)

    def pick_slower(a: dict[str, Any], b: dict[str, Any]):
        return (a, b) if float(a["latency_ms"]) >= float(b["latency_ms"]) else (b, a)

    slower, faster = pick_slower(r1, r2)
    latency_diff_ms = float(slower["latency_ms"]) - float(faster["latency_ms"])

    model_more_output = str(more_output["model"])
    model_less_output = str(less_output["model"])
    model_slower = str(slower["model"])

    results_table = (
        "| Call | Model | Input Tokens | Output Tokens | Total Tokens | Latency (ms) | Cost (paid equiv.) |\n"
        "|---|---|---|---|---|---|---|\n"
        f"| 1 | {r1['model']} | {r1['input_tokens']} | {r1['output_tokens']} | {r1['total_tokens']} | {int(round(float(r1['latency_ms'])))} | {format_money_usd(float(r1['cost_paid_tier_reference']))} |\n"
        f"| 2 | {r2['model']} | {r2['input_tokens']} | {r2['output_tokens']} | {r2['total_tokens']} | {int(round(float(r2['latency_ms'])))} | {format_money_usd(float(r2['cost_paid_tier_reference']))} |\n"
    )

    # Exactly 5 reflection sentences.
    sentences = [
        make_reflection_sentence_1(model_more_output, model_less_output),
        make_reflection_sentence_2(model_slower, latency_diff_ms),
        make_reflection_sentence_3(model_more_output),
        make_reflection_sentence_4(model_more_output),
        make_reflection_sentence_5(model_more_output),
    ]

    reflection_block = (
        "## Reflection (5 sentences)\n\n"
        "1. " + sentences[0] + "\n"
        "2. " + sentences[1] + "\n"
        "3. " + sentences[2] + "\n"
        "4. " + sentences[3] + "\n"
        "5. " + sentences[4] + "\n"
    )

    with open(readme_path, "r", encoding="utf-8") as f:
        readme_text = f.read()

    # Replace Results section block and Reflection block by markers.
    results_heading = "## Results (fill after running)"
    reflection_heading = "## Reflection (5 sentences)"
    outputs_heading = "## Outputs"

    i1 = readme_text.find(results_heading)
    i2 = readme_text.find(reflection_heading)
    i3 = readme_text.find(outputs_heading)
    if i1 == -1 or i2 == -1 or i3 == -1 or i2 <= i1 or i3 <= i2:
        raise SystemExit("ERROR: README section markers not found (cannot update safely).")

    new_results_block = results_heading + "\n\n" + results_table + "\n"
    new_readme = readme_text[:i1] + new_results_block + "\n\n" + reflection_block + "\n\n" + readme_text[i3:]

    with open(readme_path, "w", encoding="utf-8") as f:
        f.write(new_readme)

    print("README updated from results_hw1_models.json")


if __name__ == "__main__":
    main()

